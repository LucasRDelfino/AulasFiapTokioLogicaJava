package fiap.tokio.logica.exercicios;

import java.util.Scanner;

public class Aula05DESAFIO {
	public static void main(String[] args) {

		Scanner entrada = new Scanner(System.in);
		 
	    
	     System.out.println("Digite quantos dias você tem de idade ");
	     int d = entrada.nextInt();	
	     
	  
	     int anos = d/365;
	     int dmeses = d%365;
	     int meses = dmeses/30;
	     int dias = dmeses%30;
	    
	   
	 
	         
	     	 
	     
	      

	     System.out.println("Dias: "+dias+"\nAnos: "+anos+"\nMeses: "+meses);
	     
	     
		
		
	}	 
		 
		 
	 }

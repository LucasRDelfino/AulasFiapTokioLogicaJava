package fiap.tokio.logica.aulas;

public class ClasseMath {
	public static void main(String[] args) {
		
		System.out.println(Math.PI);//Numero PI
		
		System.out.println(Math.pow(5, 2));//Exponenciação
		
		System.out.println(Math.sqrt(25));//Raiz quadrada
		
		System.out.println(Math.cbrt(8));//Raiz cubica
		
		System.out.println(Math.abs(-20));//Função inversa
		
		System.out.println(Math.floor(4.2));//Arredonda para baixo
		
		System.out.println(Math.ceil(4.1));//Arredonda para baixo
		
		System.out.println(Math.round(4.5));//Arredonda aritmeticamente
		
		
		
		
		
		
		
	}
}

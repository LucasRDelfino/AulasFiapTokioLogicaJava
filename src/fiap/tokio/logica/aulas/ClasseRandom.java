package fiap.tokio.logica.aulas;

import java.util.Random;

public class ClasseRandom {
	public static void main(String[] args) {
		Random random = new Random();
		
		System.out.println(random.nextDouble());//Sorteia entre 0 e 1
	
		System.out.println(random.nextBoolean());// Sorteia true ou false
		
		System.out.println(random.nextInt(10)); // número entre 5 e 10
	
	
	
	
	
	
	
	}
}
